  // Build stats with counter animation
  function buildStats() {
    const stats = translations[currentLocale].stats;
    statsGrid.innerHTML = stats.map((text, idx) => `<div class="stat-item"><h2 class="counter" data-target="${idx===0?25:idx===1?1000:24}">0</h2><p>${text}</p></div>`).join('');
    const observer = new IntersectionObserver((entries) => {
      entries.forEach(entry => {
        if (entry.isIntersecting) {
          const counters = document.querySelectorAll('.counter');
          counters.forEach(c => {
            const target = parseInt(c.dataset.target);
            let current = 0;
            const step = Math.ceil(target / 50);
            const interval = setInterval(() => {
              current += step;
              if (current >= target) {
                c.innerText = target + (c.dataset.target == '1000' ? '+' : '');
                clearInterval(interval);
              } else c.innerText = current;
            }, 25);
          });
          observer.disconnect();
        }
      });
    }, { threshold: 0.3 });
    observer.observe(statsGrid);
  }
  
  // Gallery rendering
  function renderGallery() {
    gallerySlides.innerHTML = galleryImages.map((img, i) => `<div class="gallery-slide" data-index="${i}"><img src="${img}" loading="lazy" alt="gallery ${i}"></div>`).join('');
    document.querySelectorAll('.gallery-slide').forEach(slide => {
      slide.addEventListener('click', () => {
        const imgSrc = slide.querySelector('img').src;
        const lb = document.createElement('div');
        lb.className = 'lightbox';
        lb.innerHTML = `<img src="${imgSrc}">`;
        lb.addEventListener('click', () => lb.remove());
        document.body.appendChild(lb);
      });
    });
    updateGallery();
  }
  
  function updateGallery() {
    const slides = document.querySelectorAll('.gallery-slide');
    if (slides.length) {
      gallerySlides.style.transform = `translateX(-${currentSlide * 100}%)`;
      imageCounter.innerText = `${currentSlide + 1} / ${totalSlides}`;
    }
  }
  
  function nextSlide() {
    if (currentSlide < totalSlides - 1) { currentSlide++; updateGallery(); }
  }
  function prevSlide() {
    if (currentSlide > 0) { currentSlide--; updateGallery(); }
  }
  
